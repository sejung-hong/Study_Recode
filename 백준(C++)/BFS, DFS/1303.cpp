//1303 전쟁-전투
#include<iostream>
#include<queue>
using namespace std;
int M, N;
char BW[101][101];
bool visited[101][101] = { false, };
int num;
const int dx[4] = { -1, 0, 1,0 }, dy[4] = { 0,1,0,-1 };

void DFS(int x, int y, char bw) {
	visited[x][y] = true; //방문 완료
	num++;
	for (int i = 0; i < 4; i++) {
		int nx = x + dx[i], ny = y + dy[i];
		if (nx < 0 || ny < 0 || nx >= M || ny>= N)
			continue;
		if (visited[nx][ny] == false && BW[nx][ny] == bw) {
			DFS(nx, ny, bw);
		}
	}
}

int main() {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> N >> M;

	char bw;
	for (int i = 0; i < M; i++) {
		for (int j = 0; j < N; j++) {
			cin >> BW[i][j]; //입력
		}
	}

	int power_B = 0;
	int power_W = 0;

	for (int i = 0; i < M; i++) {
		for (int j = 0; j < N; j++) {
			if (visited[i][j] != true) {
				num = 0;
				DFS(i, j, BW[i][j]);
				if (BW[i][j] == 'B') {
					power_B += num * num;
				}
				else if (BW[i][j] == 'W') {
					power_W += num * num;
				}
			}
		}
	}

	cout << power_W << " " << power_B << "\n";
	
	return 0;
}